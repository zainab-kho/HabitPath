// scripts/gen-apercu-editor-css.js
//
// Generates lib/editor/apercuEditorCss.ts — the CSS injected into the tentap
// WebView note editor. It base64-embeds the Apercu fonts and defines all editor
// styling (font sizes, spacing, headings, the custom checkbox, etc.).
//
// The generated .ts file is huge (embedded font data) and painful to hand-edit,
// so edit the `css` template below and regenerate instead:
//
//     node scripts/gen-apercu-editor-css.js
//
// To add e.g. an italic face: drop the .otf in assets/fonts/Apercu, read + embed
// it here with another @font-face (font-style: italic), then rerun.

const fs = require("fs");
const path = require("path");

// repo root = one level up from this scripts/ folder (portable across machines)
const root = path.resolve(__dirname, "..");
const fontsDir = path.join(root, "assets/fonts/Apercu");

const medium = fs.readFileSync(path.join(fontsDir, "apercu_medium_pro.otf")).toString("base64");
const bold = fs.readFileSync(path.join(fontsDir, "apercu_bold_pro.otf")).toString("base64");

const css = `
@font-face {
  font-family: "Apercu";
  font-style: normal;
  font-weight: 400;
  src: url(data:font/otf;base64,${medium}) format("opentype");
}
@font-face {
  font-family: "Apercu";
  font-style: normal;
  font-weight: 700;
  src: url(data:font/otf;base64,${bold}) format("opentype");
}
html, body {
  /* stop the WebView from rubber-banding / overscrolling so it feels like a static view */
  overscroll-behavior: none;
  margin: 0;
}
.ProseMirror {
  font-family: "Apercu", -apple-system, sans-serif;
  font-weight: 400;
  font-size: 15px;
  line-height: 20px;
  color: #000000;
  padding: 4px 2px;
  caret-color: #000000;
  -webkit-font-smoothing: antialiased;
}
.ProseMirror p { margin: 0 0 2px 0; }
.ProseMirror strong, .ProseMirror b { font-weight: 700; }
.ProseMirror h1 { font-size: 22px; font-weight: 400; margin: 8px 0 4px; }
.ProseMirror h2 { font-size: 19px; font-weight: 400; margin: 8px 0 4px; }
.ProseMirror h3 { font-size: 17px; font-weight: 400; margin: 6px 0 4px; }
/* the note title (first line) keeps the wider gap below it */
.ProseMirror > h1:first-child { margin-bottom: 15px; }
.ProseMirror ul, .ProseMirror ol { padding-left: 22px; margin: 0 0 4px 0; }
.ProseMirror li { margin: 0; }
.ProseMirror li p { margin: 0; }
/* task list: checkbox sits on the LEFT, aligned like bullet / numbered lists */
.ProseMirror ul[data-type="taskList"] { list-style: none; padding-left: 2px; margin: 0 0 4px 0; }
.ProseMirror ul[data-type="taskList"] li { display: flex; flex-direction: row; align-items: center; gap: 6px; margin-bottom: 5px; }
.ProseMirror ul[data-type="taskList"] li > label { flex: 0 0 auto; margin: 0; user-select: none; }
/* custom checkbox: unchecked = primary[1] pink, checked = primary[0] blue with a white check */
.ProseMirror ul[data-type="taskList"] li > label input[type="checkbox"] {
  -webkit-appearance: none;
  appearance: none;
  width: 26px;
  height: 26px;
  margin: 0;
  border: 2px solid #FFA4D8;
  border-radius: 7px;
  background-color: #ffffff;
  position: relative;
  cursor: pointer;
  flex: 0 0 auto;
}
.ProseMirror ul[data-type="taskList"] li > label input[type="checkbox"]:checked {
  background-color: #75A9FF;
  border-color: #75A9FF;
}
.ProseMirror ul[data-type="taskList"] li > label input[type="checkbox"]:checked::after {
  content: "";
  position: absolute;
  left: 8px;
  top: 3px;
  width: 6px;
  height: 12px;
  border: solid #ffffff;
  border-width: 0 2.5px 2.5px 0;
  transform: rotate(45deg);
}
.ProseMirror ul[data-type="taskList"] li > div { flex: 1 1 auto; margin-top: -1px; }
.ProseMirror ul[data-type="taskList"] li > div > p { margin: 0; }
.ProseMirror blockquote {
  border-left: 3px solid #00000022;
  padding-left: 12px;
  margin: 8px 0;
  color: #00000099;
}
.ProseMirror a { color: #2563eb; text-decoration: underline; }
/* hide the placeholder text entirely */
.ProseMirror .is-editor-empty::before,
.ProseMirror p.is-empty::before,
.ProseMirror h1.is-empty::before {
  content: "" !important;
}
`;

const fileHeader =
  "// AUTO-GENERATED by scripts/gen-apercu-editor-css.js — do not edit by hand.\n" +
  "// Edit the CSS template in that script and rerun: node scripts/gen-apercu-editor-css.js\n" +
  "// Injects the app Apercu font into the tentap WebView editor so note text matches\n" +
  "// native text styled with the p1/p2/p3 families (registered in app/_layout.tsx).\n\n";

const out = fileHeader + "export const APERCU_EDITOR_CSS = " + JSON.stringify(css) + ";\n";

const dest = path.join(root, "lib/editor/apercuEditorCss.ts");
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, out);
console.log("wrote " + path.relative(root, dest) + " (" + (fs.statSync(dest).size / 1024).toFixed(0) + " KB)");
